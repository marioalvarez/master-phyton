mi_texto = "Master"
mi_texto2 = "en python"

texto_unido = mi_texto + " " + mi_texto2
print(texto_unido)

#texto entre comillas
mi_texto = '"Master"'
mi_texto2 = "en \"Python\""
texto_unido = mi_texto + " " + mi_texto2
print(texto_unido)

#valores especiales
#salto de linea
texto_unido = mi_texto + " \n" + mi_texto2
print(texto_unido)
#tabulacion
texto_unido = mi_texto + " \t" + mi_texto2
print(texto_unido)
#esconde primera variable
texto_unido = mi_texto + " \r" + mi_texto2
print(texto_unido)