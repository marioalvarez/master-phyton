#operadores de asignacion

edad = 55
print(edad)

#suma edad + 5
edad+= 5
edad = edad + 5
edad -= 5 #resta 5 sobre el valor de edad
print(edad)

#operadores de incremento y decremento

year = 2021

#incremento

year = year + 1
year+=1
#decremento

year = year - 1
year-=1

#pre incremento
year = 1 + year
print(year)
#pre decremento
year = 1 - year
print(year)